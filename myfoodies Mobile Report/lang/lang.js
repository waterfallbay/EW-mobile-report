function decidecurrentlang(lang)
{
    if(lang==="en")
    {
        currentlang = en;
        kendo.culture("en-US");
        datatablelang = dten;
    }
    else if(lang==="zh"){
        currentlang = zh;
        kendo.culture("zh-HK");
        datatablelang = dtzh;
    }
    else{
         currentlang = zh;
         kendo.culture("zh-HK");
         datatablelang = dtzh;
    }
        
    return currentlang;
}

